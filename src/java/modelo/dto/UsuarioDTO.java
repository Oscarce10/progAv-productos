package modelo.dto;

import java.io.Serializable;

/**
 *
 * @author Estudiante
 */
public class UsuarioDTO implements Serializable{
    int tipo;
    private String correo, clave;

    public UsuarioDTO(int tipo, String correo, String clave) {
        this.tipo = tipo;
        this.correo = correo;
        this.clave = clave;
    }

    
    
    public UsuarioDTO(String correo, String clave) {
        this.correo = correo;
        this.clave = clave;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }
    
    
}


